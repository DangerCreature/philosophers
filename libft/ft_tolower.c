int	ft_tolower(int c)
{
	if (('A' <= c && c <= 'Z'))
	{
		return (c + 32);
	}
	else if (-128 <= c && c <= -2)
	{
		return (c + 256);
	}
	else
	{
		return (c);
	}
}
